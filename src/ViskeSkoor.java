public class ViskeSkoor {
    int skoor;
    int viske_skoor;

    public int getSkoor() {
        return skoor;
    }

    public void setSkoor(int skoor) {
        this.skoor = skoor;
    }

    public int getViske_skoor() {
        return viske_skoor;
    }

    public void setViske_skoor(int viske_skoor) {
        this.viske_skoor = viske_skoor;
    }

    public ViskeSkoor(int skoor, int viske_skoor) {
        this.skoor = skoor;
        this.viske_skoor = viske_skoor;
    }
}
